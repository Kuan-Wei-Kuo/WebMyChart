function Rect() {
  this.left;
  this.right;
  this.top;
  this.bottom;
}

Rect.prototype.getLeft = function () {
  return this.left;
};

Rect.prototype.getRight = function () {
  return this.right;
};

Rect.prototype.getTop = function () {
  return this.top;
};

Rect.prototype.getBottom = function () {
  return this.bottom;
};
